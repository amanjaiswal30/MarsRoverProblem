public class Plateau
{
	private int length;
	private int breadth;

	Plateau(int length, int breadth)
	{
		this.length = length;
		this.breadth = breadth;
	}

	int getLength()
	{
		return this.length;
	}

	int getBreadth()
	{
		return this.breadth;
	}

	void setLength(int length)
	{
		this.length = length;
	}

	void setBreadth(int breadth)
	{
		this.breadth = breadth;
	}
}
